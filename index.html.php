  <!-- Continuous left-scrolling carousel script: smooth infinite loop and pause-on-hover -->
  <script>
    document.addEventListener('DOMContentLoaded', function() {
      const carousel = document.querySelector('.carousel');
      const slides = carousel && carousel.querySelector('.slides');
      if (!carousel || !slides) return;

      // Collect original slide nodes
      const originals = Array.from(slides.children);

      // Duplicate slides for seamless looping
      originals.forEach(node => slides.appendChild(node.cloneNode(true)));

      // Ensure slides container sizing matches number of children
      const childCount = slides.children.length;
      slides.style.width = (childCount * 100) + '%';
      Array.from(slides.children).forEach(child => {
        child.style.width = (100 / childCount) + '%';
      });

      // Smooth animation using requestAnimationFrame
      let paused = false;
      let rafId = null;
      let pos = 0; // px scrolled
      const speed = 80; // pixels per second (adjustable)
      let maxScroll = 0; // width of original content in px

      function measure() {
        // original content width is half of total (we duplicated)
        maxScroll = slides.scrollWidth / 2;
      }

      function step(now) {
        if (!step.last) step.last = now;
        const dt = now - step.last;
        step.last = now;
        if (!paused) {
          pos += (speed * dt) / 1000;
          if (pos >= maxScroll) pos = pos - maxScroll;
          slides.style.transform = `translateX(-${pos}px)`;
        }
        rafId = requestAnimationFrame(step);
      }

      // Pause on hover
      carousel.addEventListener('mouseenter', () => { paused = true; });
      carousel.addEventListener('mouseleave', () => { paused = false; });

      // Re-measure after images load and on resize
      function waitForImages() {
        const imgs = slides.querySelectorAll('img');
        const promises = Array.from(imgs).map(img => {
          if (img.complete) return Promise.resolve();
          return new Promise(res => { img.addEventListener('load', res); img.addEventListener('error', res); });
        });
        return Promise.all(promises);
      }

      function start() {
        measure();
        if (!rafId) {
          step.last = performance.now();
          rafId = requestAnimationFrame(step);
        }
      }

      function stop() {
        if (rafId) { cancelAnimationFrame(rafId); rafId = null; }
      }

      waitForImages().then(() => {
        measure();
        start();
      });

      window.addEventListener('resize', () => {
        measure();
      });
    });
  </script>
